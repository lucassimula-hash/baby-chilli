export type ChilliLogoProps = {
  type?: ChilliLogoType;
  color?: ChilliLogoColor;
  /** Width in pixels. Height is derived from the aspect ratio. Default 32. */
  size?: number;
  /** Override the fill color directly. Takes precedence over `color`. */
  fill?: string;
  className?: string;
};

export function ChilliLogo({
  type = "symbol",
  color = "brand",
  size = 32,
  fill,
  className,
}: ChilliLogoProps) {
  const spec = SPECS[type];
  const ratio = spec.viewBoxHeight / spec.viewBoxWidth;
  const width = size;
  const height = size * ratio;
  const fillColor = fill ?? CHILLI_FILL[color];

  return (
    <svg
      role="img"
      aria-label="Chilli"
      width={width}
      height={height}
      viewBox={`0 0 ${spec.viewBoxWidth} ${spec.viewBoxHeight}`}
      xmlns="http://www.w3.org/2000/svg"
      className={cn("block", className)}
    >
      <path d={spec.d} fill={fillColor} fillRule={spec.fillRule ?? "nonzero"} />
    </svg>
  );
}
